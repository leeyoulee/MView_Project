    @Override
    public void onActivityCreated(Bundle b) {
        super.onActivityCreated(b);

        qnaView = getView().findViewById(R.id.qnaView);
        qnaView.setHasFixedSize(true);
        LinearLayoutManager layoutManager_notice = new LinearLayoutManager(getContext());
        layoutManager_notice.setOrientation(LinearLayoutManager.VERTICAL);
        qnaView.setLayoutManager(layoutManager_notice);
        qnaViewdatas = new ArrayList<>();
        qnaloadData();
        DividerItemDecoration dividerItemDecoration = new DividerItemDecoration(getContext(), LinearLayoutManager.VERTICAL);
        dividerItemDecoration.setDrawable(getContext().getResources().getDrawable(R.drawable.divider_recyclerview));
        qnaView.addItemDecoration(dividerItemDecoration);

    }

    private void qnaloadData() {
        StringRequest stringRequest = new StringRequest(Request.Method.GET, URL_QNA, new Response.Listener<String>() {
            @Override
            public void onResponse(String response) {
                try {
                    JSONObject jsonObject = new JSONObject(response);
                    JSONArray array = jsonObject.getJSONArray("response");
                    for (int i = 0; i < array.length(); i++){
                        JSONObject obj = array.getJSONObject(i);
                        qna_data qna_Data = new qna_data(obj.getString("qnaTitle"),obj.getString("qnaNo"));
                        qnaViewdatas.add(qna_Data);
                    }
                    qna_adapter adapter = new qna_adapter(getContext(), qnaViewdatas);
                    qnaView.setAdapter(adapter);
                } catch (JSONException e) {
                    e.printStackTrace();
                }
            }
        }, new Response.ErrorListener() {
            @Override
            public void onErrorResponse(VolleyError error) {
                Toast.makeText(getContext(), "Error" + error.toString(), Toast.LENGTH_SHORT).show();
            }
        });
        RequestQueue requestQueue = Volley.newRequestQueue(getContext());
        requestQueue.add(stringRequest);
    }

